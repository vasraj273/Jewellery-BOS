  updated_at         timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_calendar_events_date ON calendar_events(event_date, is_active);
-- Reject duplicate active events with the same title on the same date (case-insensitive).
CREATE UNIQUE INDEX IF NOT EXISTS uq_calendar_events_title_date
  ON calendar_events(lower(title), event_date) WHERE is_active;

