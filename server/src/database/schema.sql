-- ============================================================
-- CRM Core (M4) — Lead management
-- ============================================================

-- Lead source + status catalogs (admin-editable later; seeded on boot).
CREATE TABLE IF NOT EXISTS lead_sources (
  id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  label       text NOT NULL,
  sort_order  integer NOT NULL DEFAULT 100,
  is_active   boolean NOT NULL DEFAULT true,
  created_at  timestamptz NOT NULL DEFAULT now(),
  updated_at  timestamptz NOT NULL DEFAULT now()
);
CREATE UNIQUE INDEX IF NOT EXISTS uq_lead_sources_label ON lead_sources(lower(label));

CREATE TABLE IF NOT EXISTS lead_statuses (
  id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  label       text NOT NULL,
  sort_order  integer NOT NULL DEFAULT 100,
  is_active   boolean NOT NULL DEFAULT true,
  is_terminal text,                              -- 'converted' | 'lost' | null
  created_at  timestamptz NOT NULL DEFAULT now(),
  updated_at  timestamptz NOT NULL DEFAULT now()
);
CREATE UNIQUE INDEX IF NOT EXISTS uq_lead_statuses_label ON lead_statuses(lower(label));

CREATE TABLE IF NOT EXISTS leads (
  id                     bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  lead_code              text NOT NULL UNIQUE,
  name                   text NOT NULL,
  mobile                 text NOT NULL,
  email                  text,
  occasion               text,
  budget                 numeric(12,2),
  notes                  text,
  source_id              bigint REFERENCES lead_sources(id) ON DELETE SET NULL,
  status_id              bigint REFERENCES lead_statuses(id) ON DELETE SET NULL,
  priority               text NOT NULL DEFAULT 'medium',   -- low | medium | high
  assigned_user_id       bigint REFERENCES users(id) ON DELETE SET NULL,
  created_by_user_id     bigint REFERENCES users(id) ON DELETE SET NULL,
  converted_customer_id  bigint,                            -- M5 customers (loose link)
  converted_quotation_id bigint,                            -- set when a quote is raised
  converted_at           timestamptz,                       -- when the lead became Converted
  next_followup_at       timestamptz,
  is_converted           boolean NOT NULL DEFAULT false,
  is_lost                boolean NOT NULL DEFAULT false,
  created_at             timestamptz NOT NULL DEFAULT now(),
  updated_at             timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_leads_mobile     ON leads(mobile);
CREATE INDEX IF NOT EXISTS idx_leads_assigned   ON leads(assigned_user_id);
CREATE INDEX IF NOT EXISTS idx_leads_status     ON leads(status_id);
CREATE INDEX IF NOT EXISTS idx_leads_source     ON leads(source_id);
CREATE INDEX IF NOT EXISTS idx_leads_followup   ON leads(next_followup_at);
CREATE INDEX IF NOT EXISTS idx_leads_created    ON leads(created_at DESC);

CREATE TABLE IF NOT EXISTS lead_followups (
  id                 bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  lead_id            bigint NOT NULL REFERENCES leads(id) ON DELETE CASCADE,
  followup_date      timestamptz NOT NULL DEFAULT now(),
  notes              text NOT NULL,
  created_by_user_id bigint REFERENCES users(id) ON DELETE SET NULL,
  created_at         timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_lead_followups_lead ON lead_followups(lead_id, created_at DESC);

-- ============================================================
-- Advanced CRM (M5) — Customers, Timeline, Reminders
-- ============================================================
CREATE TABLE IF NOT EXISTS customers (
  id                bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_code     text NOT NULL UNIQUE,
  name              text NOT NULL,
  mobile            text NOT NULL,
  email             text,
  address           text,
  birthday          date,
  anniversary       date,
  ring_size         text,
  preferred_metal   text,
  budget_range      text,
  notes             text,
  assigned_user_id  bigint REFERENCES users(id) ON DELETE SET NULL,
  source_lead_id    bigint REFERENCES leads(id) ON DELETE SET NULL,
  created_at        timestamptz NOT NULL DEFAULT now(),
  updated_at        timestamptz NOT NULL DEFAULT now()
);
-- Mobile is the dedupe identity. Unique so conversion can't create twins.
CREATE UNIQUE INDEX IF NOT EXISTS uq_customers_mobile ON customers(mobile);
CREATE INDEX IF NOT EXISTS idx_customers_assigned ON customers(assigned_user_id);
CREATE INDEX IF NOT EXISTS idx_customers_code     ON customers(customer_code);

CREATE TABLE IF NOT EXISTS customer_events (
  id             bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_id    bigint NOT NULL REFERENCES customers(id) ON DELETE CASCADE,
  event_type     text NOT NULL,        -- lead_converted | quotation_sent | reminder_added | profile_updated | note | ...
  title          text NOT NULL,
  notes          text,
  actor_user_id  bigint REFERENCES users(id) ON DELETE SET NULL,
  created_at     timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_customer_events_cust ON customer_events(customer_id, created_at DESC);

CREATE TABLE IF NOT EXISTS reminder_tasks (
  id                 bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_id        bigint REFERENCES customers(id) ON DELETE CASCADE,
  lead_id            bigint REFERENCES leads(id) ON DELETE SET NULL,
  title              text NOT NULL,
  notes              text,
  due_at             timestamptz NOT NULL,
  status             text NOT NULL DEFAULT 'pending',  -- pending | done
  assigned_user_id   bigint REFERENCES users(id) ON DELETE SET NULL,
  created_by_user_id bigint REFERENCES users(id) ON DELETE SET NULL,
  done_at            timestamptz,
  created_at         timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_reminders_due       ON reminder_tasks(due_at);
CREATE INDEX IF NOT EXISTS idx_reminders_assigned  ON reminder_tasks(assigned_user_id, status);

-- ============================================================
-- HRMS (M6) — Employees, Attendance, Leaves
-- ============================================================
CREATE TABLE IF NOT EXISTS employees (
  id                  bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  employee_code       text NOT NULL UNIQUE,
  user_id             bigint UNIQUE REFERENCES users(id) ON DELETE SET NULL, -- one profile per user
  full_name           text NOT NULL,
  email               text,
  mobile              text,
  role                text,                  -- mirrors user role at create time
  department          text,
  designation         text,
  joining_date        date,
  reporting_manager_id bigint REFERENCES employees(id) ON DELETE SET NULL,
  employment_status   text NOT NULL DEFAULT 'active', -- active | on_leave | resigned | terminated
  emergency_contact   text,
  address             text,
  notes               text,
  is_active           boolean NOT NULL DEFAULT true,
  created_at          timestamptz NOT NULL DEFAULT now(),
  updated_at          timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_employees_user ON employees(user_id);

CREATE TABLE IF NOT EXISTS attendance (
  id               bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  employee_id      bigint NOT NULL REFERENCES employees(id) ON DELETE CASCADE,
  attendance_date  date NOT NULL,
  status           text NOT NULL DEFAULT 'present',  -- present | half_day | absent | leave
  check_in_time    text,
  check_out_time   text,
  notes            text,
  marked_by_user_id bigint REFERENCES users(id) ON DELETE SET NULL,
  created_at       timestamptz NOT NULL DEFAULT now(),
  updated_at       timestamptz NOT NULL DEFAULT now()
);
-- One row per employee per day.
CREATE UNIQUE INDEX IF NOT EXISTS uq_attendance_emp_date ON attendance(employee_id, attendance_date);
CREATE INDEX IF NOT EXISTS idx_attendance_date ON attendance(attendance_date DESC);

CREATE TABLE IF NOT EXISTS leaves (
  id                bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  employee_id       bigint NOT NULL REFERENCES employees(id) ON DELETE CASCADE,
  leave_type        text NOT NULL,                   -- casual | sick | emergency | paid | unpaid
  start_date        date NOT NULL,
  end_date          date NOT NULL,
  reason            text,
  status            text NOT NULL DEFAULT 'pending', -- pending | approved | rejected
  decided_by_user_id bigint REFERENCES users(id) ON DELETE SET NULL,
  decided_at        timestamptz,
  created_at        timestamptz NOT NULL DEFAULT now(),
  updated_at        timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_leaves_employee ON leaves(employee_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_leaves_status   ON leaves(status);
CREATE INDEX IF NOT EXISTS idx_leaves_dates    ON leaves(start_date, end_date);

-- ============================================================
-- Advanced HRMS (M7)
-- ============================================================
CREATE TABLE IF NOT EXISTS shifts (
  id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  shift_name  text NOT NULL,
  start_time  text,
  end_time    text,
  weekly_off  text,                  -- e.g. 'Sunday'
  is_active   boolean NOT NULL DEFAULT true,
  created_at  timestamptz NOT NULL DEFAULT now(),
  updated_at  timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS tasks (
  id                 bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title              text NOT NULL,
  description        text,
  assigned_to_user_id bigint REFERENCES users(id) ON DELETE SET NULL,
  assigned_by_user_id bigint REFERENCES users(id) ON DELETE SET NULL,
  due_date           date,
  priority           text NOT NULL DEFAULT 'medium',  -- low | medium | high | urgent
  linked_entity_type text,                            -- lead | customer | quotation
  linked_entity_id   text,
  status             text NOT NULL DEFAULT 'pending',  -- pending | in_progress | completed | cancelled
  completed_at       timestamptz,
  created_at         timestamptz NOT NULL DEFAULT now(),
  updated_at         timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_tasks_assignee ON tasks(assigned_to_user_id, status);
CREATE INDEX IF NOT EXISTS idx_tasks_due      ON tasks(due_date);

CREATE TABLE IF NOT EXISTS incentives (
  id                  bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  employee_id         bigint NOT NULL REFERENCES employees(id) ON DELETE CASCADE,
  type                text NOT NULL DEFAULT 'fixed',   -- percentage | fixed | target_bonus
  percentage          numeric(6,2),
  fixed_amount        numeric(12,2),
  amount              numeric(12,2),                   -- resolved payout amount
  linked_quotation_id bigint,
  linked_customer_id  bigint,
  notes               text,
  status              text NOT NULL DEFAULT 'draft',   -- draft | approved | paid
  approved_by_user_id bigint REFERENCES users(id) ON DELETE SET NULL,
  approved_at         timestamptz,
  paid_at             timestamptz,
  created_by_user_id  bigint REFERENCES users(id) ON DELETE SET NULL,
  created_at          timestamptz NOT NULL DEFAULT now(),
  updated_at          timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_incentives_emp    ON incentives(employee_id, status);
CREATE INDEX IF NOT EXISTS idx_incentives_status ON incentives(status);

CREATE TABLE IF NOT EXISTS employee_compensation (
  id                 bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  employee_id        bigint NOT NULL UNIQUE REFERENCES employees(id) ON DELETE CASCADE,
  salary_type        text NOT NULL DEFAULT 'monthly',  -- monthly | daily | contract
  base_salary        numeric(12,2) DEFAULT 0,
  allowance          numeric(12,2) DEFAULT 0,
  deduction          numeric(12,2) DEFAULT 0,
  overtime_rate      numeric(12,2) DEFAULT 0,
  commission_eligible boolean NOT NULL DEFAULT false,
  notes              text,
  updated_by_user_id bigint REFERENCES users(id) ON DELETE SET NULL,
  created_at         timestamptz NOT NULL DEFAULT now(),
  updated_at         timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS employee_documents (
  id               bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  employee_id      bigint NOT NULL REFERENCES employees(id) ON DELETE CASCADE,
  document_name    text NOT NULL,
  category         text NOT NULL DEFAULT 'Other',   -- Identity | Employment | Legal | Other
  upload_url       text NOT NULL,
  uploaded_by_user_id bigint REFERENCES users(id) ON DELETE SET NULL,
  is_active        boolean NOT NULL DEFAULT true,
  created_at       timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_emp_docs ON employee_documents(employee_id, is_active);

-- ============================================================
-- Audit log (M2 foundation; extend in later milestones)
-- ============================================================
CREATE TABLE IF NOT EXISTS audit_events (
  id              bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  actor_user_id   bigint REFERENCES users(id) ON DELETE SET NULL,
  actor_email     text,                          -- denormalised so log survives user delete
  action          text NOT NULL,                 -- e.g. 'auth.login', 'quotation.create', 'user.create'
  entity_type     text,                          -- 'quotation' | 'user' | 'auth' ...
  entity_id       text,                          -- string for portability across PK types
  metadata        jsonb NOT NULL DEFAULT '{}'::jsonb,
  ip              text,
  user_agent      text,
  created_at      timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_audit_actor   ON audit_events(actor_user_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_audit_action  ON audit_events(action, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_audit_entity  ON audit_events(entity_type, entity_id, created_at DESC);


CREATE TABLE IF NOT EXISTS quotations (
  id                  bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  quote_id            text    NOT NULL UNIQUE,
  status              text    NOT NULL DEFAULT 'draft',   -- draft | sent | accepted | expired

  -- Customer
  customer_name       text    NOT NULL,
  customer_mobile     text,
  customer_email      text,
  occasion            text,

  -- Product
  product_name        text,
  product_category    text,
  product_description text,
  product_image_path  text,

  -- Metal
  metal_type          text,
  metal_color         text,
  purity              text,
  gross_weight        double precision DEFAULT 0,
  net_weight          double precision DEFAULT 0,

  -- Diamond
  diamond_type        text,
  diamond_shape       text,
  diamond_carat       double precision DEFAULT 0,
  diamond_clarity     text,
  diamond_color       text,

  -- Gemstone
  gemstone            text,
  gemstone_carat      double precision DEFAULT 0,

  -- Certifications
  hallmark            text,
  certification       text,
  setting_style       text,

  -- Rates snapshot (frozen at creation)
  gold_rate_per_gram      double precision DEFAULT 0,
  diamond_rate_per_carat  double precision DEFAULT 0,
  gemstone_rate_per_carat double precision DEFAULT 0,

  -- Making
  making_charge_type      text DEFAULT 'per_gram',
  making_charge_value     double precision DEFAULT 0,

  -- Extras
  hallmark_charge         double precision DEFAULT 0,
  certification_charge    double precision DEFAULT 0,
  shipping_charge         double precision DEFAULT 0,

  -- Computed
  gold_cost           double precision DEFAULT 0,
  diamond_cost        double precision DEFAULT 0,
  gemstone_cost       double precision DEFAULT 0,
  making_charge       double precision DEFAULT 0,
  subtotal            double precision DEFAULT 0,
  gst_rate            double precision DEFAULT 0.03,
  gst_amount          double precision DEFAULT 0,
  final_price         double precision DEFAULT 0,

  -- Meta
  pricing_location    text,
  sales_executive     text,
  valid_till          date,
  notes               text,
  pdf_path            text,

  -- WhatsApp delivery
  whatsapp_status     text,
  whatsapp_message_id text,
  whatsapp_sent_at    timestamptz,
  whatsapp_error      text,

  -- Ownership (M2 — RBAC visibility scoping)
  owner_user_id       bigint REFERENCES users(id) ON DELETE SET NULL,

  created_at          timestamptz NOT NULL DEFAULT now(),
  updated_at          timestamptz NOT NULL DEFAULT now()
);
-- idx_quotations_owner is created in connection.js after the column migration
