  id                 bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title              text NOT NULL,
  description        text,
  event_date         date NOT NULL,
  category           text NOT NULL DEFAULT 'general', -- meeting | exhibition | audit | holiday | promotion | training | general
