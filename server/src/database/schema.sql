-- ============================================================
-- M10 — Finance + Accounts + Billing
--   All brand-new tables (no ALTER on existing tables) → inline indexes safe.
--   Double-entry capable: journal_entries (header) + journal_lines (DR/CR).
-- ============================================================

-- Chart-of-accounts top-level groups.
CREATE TABLE IF NOT EXISTS account_groups (
  id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        text NOT NULL,
  nature      text NOT NULL,            -- asset | liability | income | expense | equity
  sort_order  integer NOT NULL DEFAULT 100,
  is_active   boolean NOT NULL DEFAULT true,
  created_at  timestamptz NOT NULL DEFAULT now()
);
CREATE UNIQUE INDEX IF NOT EXISTS uq_account_groups_name ON account_groups(lower(name));

-- Ledger accounts (chart of accounts). `code` is the stable key the posting
-- engine resolves system accounts by (CASH, BANK, AR, AP, SALES, GST, …).
CREATE TABLE IF NOT EXISTS accounts (
  id                 bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code               text NOT NULL UNIQUE,
  name               text NOT NULL,
  group_id           bigint REFERENCES account_groups(id) ON DELETE SET NULL,
  nature             text NOT NULL,        -- denormalised from group for fast posting
  is_system          boolean NOT NULL DEFAULT false,  -- system accounts can't be deleted
  is_cash_bank       boolean NOT NULL DEFAULT false,  -- selectable as a payment account
  is_active          boolean NOT NULL DEFAULT true,
  created_by_user_id bigint REFERENCES users(id) ON DELETE SET NULL,
  created_at         timestamptz NOT NULL DEFAULT now(),
  updated_at         timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_accounts_group ON accounts(group_id);
CREATE INDEX IF NOT EXISTS idx_accounts_active ON accounts(is_active, name);

-- Double-entry journal header.
CREATE TABLE IF NOT EXISTS journal_entries (
  id                 bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  entry_code         text NOT NULL UNIQUE,            -- JE-YYYY-NNNN
  entry_date         date NOT NULL DEFAULT current_date,
  narration          text,
  source             text NOT NULL DEFAULT 'manual',  -- manual | payment | supplier_payment | expense | invoice
  ref_type           text,
  ref_id             text,
  created_by_user_id bigint REFERENCES users(id) ON DELETE SET NULL,
  created_at         timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_journal_date   ON journal_entries(entry_date DESC);
CREATE INDEX IF NOT EXISTS idx_journal_source ON journal_entries(source, created_at DESC);

-- Journal lines (each entry's debit/credit rows; service enforces DR = CR).
CREATE TABLE IF NOT EXISTS journal_lines (
  id                bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  journal_entry_id  bigint NOT NULL REFERENCES journal_entries(id) ON DELETE CASCADE,
  account_id        bigint NOT NULL REFERENCES accounts(id) ON DELETE RESTRICT,
  debit             numeric(14,2) NOT NULL DEFAULT 0,
  credit            numeric(14,2) NOT NULL DEFAULT 0,
  created_at        timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_journal_lines_entry   ON journal_lines(journal_entry_id);
CREATE INDEX IF NOT EXISTS idx_journal_lines_account ON journal_lines(account_id, created_at DESC);

-- Payments — unified for customer receipts (in) and supplier payments (out).
CREATE TABLE IF NOT EXISTS payments (
  id                 bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  payment_code       text NOT NULL UNIQUE,            -- PAY-YYYY-NNNN
  party_type         text NOT NULL,                   -- customer | supplier
  direction          text NOT NULL,                   -- in (receipt) | out (payment)
  sales_order_id     bigint REFERENCES sales_orders(id) ON DELETE SET NULL,
  purchase_id        bigint REFERENCES purchases(id) ON DELETE SET NULL,
  customer_id        bigint REFERENCES customers(id) ON DELETE SET NULL,
  supplier_id        bigint REFERENCES suppliers(id) ON DELETE SET NULL,
  amount             numeric(14,2) NOT NULL,
  mode               text NOT NULL DEFAULT 'cash',    -- cash | bank | upi | card | other
  account_id         bigint REFERENCES accounts(id) ON DELETE SET NULL, -- cash/bank account hit
  paid_at            date NOT NULL DEFAULT current_date,
  notes              text,
  journal_entry_id   bigint REFERENCES journal_entries(id) ON DELETE SET NULL,
  created_by_user_id bigint REFERENCES users(id) ON DELETE SET NULL,
  created_at         timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_payments_so       ON payments(sales_order_id);
CREATE INDEX IF NOT EXISTS idx_payments_purchase ON payments(purchase_id);
CREATE INDEX IF NOT EXISTS idx_payments_supplier ON payments(supplier_id);
CREATE INDEX IF NOT EXISTS idx_payments_party    ON payments(party_type, created_at DESC);

-- Expenses.
