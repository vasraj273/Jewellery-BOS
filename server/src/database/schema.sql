  weight            double precision DEFAULT 0,
  reason            text,
  ref_type          text,                     -- purchase | quotation | manual
  ref_id            text,
  actor_user_id     bigint REFERENCES users(id) ON DELETE SET NULL,
  created_at        timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_inv_moves_item ON inventory_movements(inventory_item_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_inv_moves_type ON inventory_movements(movement_type, created_at DESC);

