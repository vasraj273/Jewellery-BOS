CREATE TABLE IF NOT EXISTS leads (
  id                     bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  lead_code              text NOT NULL UNIQUE,
  name                   text NOT NULL,
  mobile                 text NOT NULL,
  email                  text,
  occasion               text,
  budget                 numeric(12,2),
  notes                  text,
